// Write a java Program to perform right shift on given integer by 1 Position.

// Examples-1:
// -----------
// Input=4
// Output=2

import java.util.*;
public class RightShift
{
    public static void main(String[] args)
    {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        System.out.println(n>>1);
    }
}